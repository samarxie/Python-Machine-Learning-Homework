/*
Q3:
给定一个数组A[0,1,...,n-1],请构建一个数组B[0,1,...,n-1],
其中B中的元素B[i]=A[0]*A[1]*...*A[i-1]*A[i+1]*...*A[n-1]。不能使用除法。
-----------------
分析:由于不能使用除法所以我们无法将所有元素相乘，然后分别除以每个元素。
我们可以定义 C[i] = A[0]*A[1]*...*A[i-1],D[i] = A[i+1]*...*A[n-1]
我们可以得到递推公式:C[i+1]=C[i]*A[i],D[i]=A[i+1]*D[i+1]
B[i] = C[i]*D[i]
*/